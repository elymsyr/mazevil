def png_to_csv(input_folder):
    if not os.path.exists('Data'):
        os.makedirs('Data')
    
    png_files = glob.glob(os.path.join(input_folder, '*.png'))
    for png_file in png_files:
        xml_folder = os.path.dirname(png_file)
        xml_df = xml_to_csv(xml_folder)
        csv_filename = os.path.join('Data', os.path.splitext(os.path.basename(png_file))[0] + '.csv')
        xml_df.to_csv(csv_filename, index=False)